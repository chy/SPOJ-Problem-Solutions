import java.util.*;

class hangover{

	public static void main(String [] args){
		Scanner in = new Scanner(System.in);
		while(true){		
		double test = in.nextDouble();
		if(test == 0.0){break;}
		double hangover = 0.5;
		int n = 1;
		while(hangover < test){
			n++;
			hangover += 1.0/(n+1);
		}
		System.out.println(n + " card(s)");
		
		}
	}
}
